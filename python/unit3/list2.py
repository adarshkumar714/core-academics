# string manipulation functinos
s = "hello world"

s.upper() # this function changes the string to upper case
s.lower() # this function changes the string to lower case
s.strip() # this function removes the leading and trailing whitespaces
s.split() # this function splits the string into a list of words
s.replace("hello","bye") # this function replaces the word "hello" with "bye"
s.count("hello") # this function counts the number of times the word "hello" appears in the string
s.find("hello") # this function finds the index of the first occurrence of the word "hello" in the string
s.startswith("hello") # this function checks if the string starts with the word "hello"
s.endswith("hello") # this function checks if the string ends with the word "hello"


print(s)



